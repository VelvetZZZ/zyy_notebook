public class ex02 {
    public static void main(String[] args) {
        float a = 3.14f; // 正确，注意后缀f
        // double b = 3.14f; // 错误，float不能直接赋值给double
        double c = 3.14; // 正确，默认是double类型
        char d = 'A'; // 正确
        // char e = 'AB'; // 错误，char只能存储单个字符
        boolean f = true; // 正确
        boolean g = false; // 正确
        // boolean h = 1; // 错误，boolean不能存储数字
    }
    
}
